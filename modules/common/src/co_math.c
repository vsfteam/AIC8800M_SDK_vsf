/*
 * FUNCTION DEFINITIONS
 ****************************************************************************************
 */
uint32_t co_crc32(uint32_t addr, uint32_t len, uint32_t crc)
{
    while (len--)
    {
        crc = (crc << 8) ^ crc_tab[(crc >> 24) ^ co_read8p(addr++)];
    }

    return crc;
}

uint8_t co_crc8(uint8_t *pdata, uint32_t len, uint8_t crc)
{
    while (len --)
    {
        crc = crc8_tab[(crc ^ *pdata++) & 0xFF];
    }

    return crc;
}


